CREATE TABLE Bat (
    Bat_ID INT PRIMARY KEY,
    unique_3T_identifier TEXT,
    Bat_voucher TEXT,
    Bat_family TEXT,
    Bat_genus TEXT,
    Bat_species_epithet TEXT,
    Bat_species TEXT,
    Bat_identified_by TEXT,
    Sex TEXT,
    Age TEXT,
    Reproductive_status TEXT,
    Body_mass FLOAT,
    Forearm FLOAT,
    Bat_genetic_information TEXT,
    Diet TEXT,
    Roosting_ecology TEXT,
    Country TEXT,
    Territory TEXT,
    Settlement TEXT,
    Location TEXT,
    Open_Close TEXT,
    Latitude FLOAT,
    Longitude FLOAT,
    Climate_zone TEXT,
    Day INT,
    Month INT,
    Year INT,
    Contact_email TEXT,
    Comments TEXT,
    Screened_by TEXT,
    Voucher_slides TEXT,
    Isolate TEXT,
    Sequences TEXT,
    Reference TEXT
);

CREATE TABLE BatFly (
    BatFly_ID INT PRIMARY KEY,
    consecutive_fly_ID TEXT,
    Bat_fly_family TEXT,
    Bat_fly_genus TEXT,
    Bat_fly_species_epithet TEXT,
    Bat_fly_species TEXT,
    Bat_fly_identified_by TEXT,
    Bat_fly_sex TEXT,
    Bat_fly_ecomorphology TEXT,
    Bat_fly_ecology TEXT,
    Bat_fly_wing TEXT
);

CREATE TABLE Infection (
    Infection_ID INT PRIMARY KEY,
    Bat_ID INT,
    BatFly_ID INT,
    Laboul_infection TEXT,
    Double_infection TEXT,
    Laboul_genus_1 TEXT,
    Laboul_species_1 TEXT,
    Position_infected_1 TEXT,
    Laboul_genus_2 TEXT,
    Laboul_species_2 TEXT,
    Position_infected_2 TEXT,
    Labouls_identified_by TEXT,
    FOREIGN KEY (Bat_ID) REFERENCES Bat(Bat_ID),
    FOREIGN KEY (BatFly_ID) REFERENCES BatFly(BatFly_ID)
);
